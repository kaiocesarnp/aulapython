# DICIONÁRIOS

# Um dicionário é uma coleção não-ordenada de “valores” indexados por “chaves” {}.
# Dicionários se parecem com listas, mas são mais versáteis

dic = {'chave1':'valor1','chave2':'valor2'}
print(dic['chave2'])

# ------------------------

#Criaando uma nova chave:
dic['chave3'] = 'valor3'
print(dic)

